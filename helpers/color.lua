love.color = {}

love.color.get = function(color)
  assert(type(color) == "string", "Function 'set': parameter must be a string.")

  local rgb = love.color[color]
  if rgb then
    return { rgb[1] / 255, rgb[2] / 255, rgb[3] / 255 }
  end
  return {}
end

love.color.set = function(color, alpha)
  alpha = alpha or 1
  assert(type(color) == "string", "Function 'set': parameter must be a string.")
  assert(type(alpha) == "number", "Function 'set': parameter must be a number.")

  local rgb = love.color[color]
  if rgb then
    love.graphics.setColor(rgb[1] / 255, rgb[2] / 255, rgb[3] / 255, alpha)
  end
end

love.color.reset = function()
  love.graphics.setColor(1, 1, 1, 1)
end

